    private void mSpinnerPozicijaStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_mSpinnerPozicijaStateChanged
        int p = (int) mSpinnerPozicija.getValue();
        if (mIzbiraKraja.getItemAt(p) != null) {
            mLabelSporocila.setText("Sporocila: " + mIzbiraKraja.getItemAt(p));
        } else {
            mLabelSporocila.setText("Sporocila: Ni elementa");
        }
    }//GEN-LAST:event_mSpinnerPozicijaStateChanged

