    private void gumbiCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gumbiCB
        String txt = ((AbstractButton) evt.getSource()).getText();
        status.setText(status.getText() + txt.charAt(txt.length()-1));
        String prevTxt = mIzpisToolBar1.getText();
        mIzpisToolBar1.setText(prevTxt.length() == 0 ? "\"" + txt + "\"": prevTxt + ", \"" + txt + "\"");
    }//GEN-LAST:event_gumbiCB

    private void izvediCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_izvediCB
        if (mRBDodaj.isSelected()) {
            mLabelSporocila.setText("Sporocila: Dodajam...");
            String txt = mVpisKraja.getText();
            if (txt.length() != 0) {
                boolean lahkoDodam = true;
                for (int i = 0; i < mIzbiraKraja.getItemCount(); i++) {
                    if (txt.equals(mIzbiraKraja.getItemAt(i))) {
                        lahkoDodam = false;
                    }
                }
                if (!mCBDvojnik.isSelected() || lahkoDodam) {
                    mIzbiraKraja.addItem(txt);
                } else {
                    mLabelSporocila.setText("Sporocila: Dvojnik.");
                }
            }
        } else if (mRBOdstraniPrvega.isSelected()) {
            if (mIzbiraKraja.getItemCount() != 0) {
                mLabelSporocila.setText("Sporocila: Brisem prvega...");
                mIzbiraKraja.removeItemAt(0);
            }
        } else {
            if (mIzbiraKraja.getItemCount() != 0) {
                mLabelSporocila.setText("Sporocila: Brisem izbranega...");
                mIzbiraKraja.removeItemAt(mIzbiraKraja.getSelectedIndex());
            }
        }
    }//GEN-LAST:event_izvediCB

