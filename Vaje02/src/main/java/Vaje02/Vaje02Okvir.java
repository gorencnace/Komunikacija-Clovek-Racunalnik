    private void najdiCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_najdiCB
        try {
            String str = jTextPane1.getDocument().getText(0, jTextPane1.getDocument().getLength());
            String isci = jTextField1.getText();
            int i = str.indexOf(isci);
            jTextPane1.select(str.indexOf(isci), str.indexOf(isci) + isci.length());
            jTextPane1.replaceSelection(jTextField2.getText());
            jTextPane1.setCaretPosition(i);
            jTextPane1.grabFocus();
        } catch (Exception e) {}
    }//GEN-LAST:event_najdiCB

