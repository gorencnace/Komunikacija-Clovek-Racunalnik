    private void shraniCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shraniCB
        int val = odpriDialog.showSaveDialog(this);
        if (val == JFileChooser.APPROVE_OPTION) {
            File f = odpriDialog.getSelectedFile();
            try {
                jTextPane1.write(new FileWriter(f));
            } catch (Exception e) {}
        }
    }//GEN-LAST:event_shraniCB

    private void barvaBesediloCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barvaBesediloCB
        Color c = JColorChooser.showDialog(this, "Izberi barvo besedila", Color.yellow);
        StyledDocument sd = jTextPane1.getStyledDocument();
        Style s = jTextPane1.addStyle("Ospredje", null);
        StyleConstants.setForeground(s, c);
        sd.setCharacterAttributes(jTextPane1.getSelectionStart(),
                jTextPane1.getSelectionEnd() - jTextPane1.getSelectionStart(), s, true);
    }//GEN-LAST:event_barvaBesediloCB

